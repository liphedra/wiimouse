Result COMPS_Init( void )
{
    Result result = COMPS_RESULT_INIT();
    uint32 i;

    LOG_Printf("Initializing components\n");

    for( i = 0; i < COMPS_MODULE_COUNT; i++ )
    {
        if( compsModules[i].Init != NULL && RESULT_SUCCESS(result, compsModules[i].Init()) )
        {
            compsModules[i].IsInitialized = TRUE;
        }
        else
        {
           return result;
        }
    }

    LOG_RegisterModule(MOD_MGR_COMPS, NULL_MOD, TRUE);


    return COMPS_RESULT(COMPS_RESULT_SUCCESS);
}



